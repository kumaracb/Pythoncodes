# Loop ?
# # Repeat a block of code multiple times

for i in range(1, 10): # 1-9
    print("Hello PythonWorld")
    print(i)

print("----------------")

for i in range(5): # 0-4
    print(i)

print("----------------")

for i in range(1,5): # 1-4
    print(i)

print("----------------")

for i in range(3,5): # 3-4
    print(i)

print("----------------")

for i in range(1,10,2): # 1-9,3,5,7,9
    print(i)

